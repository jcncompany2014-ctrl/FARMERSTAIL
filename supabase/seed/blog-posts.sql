연 44만원 차이는 작지 않아요. 다만 이 표는 *같은 영양 등급* 기준이에요. 정기배송 가격에 맞춘다고 단품에서 등급을 내리면 영양 손실이 더 커요.

## 정기배송을 고를 때 봐야 할 5가지

1. **유연성** — 일시중지·건너뛰기·해지가 1~2 탭에 되는가
2. **양·구성 변경** — 매 회차마다 양과 단백질을 바꿀 수 있는가
3. **AAFCO/FEDIAF 완전영양식 충족** — 라벨에 명시되어 있어야 해요
4. **첫 박스 보호 정책** — 첫 4주는 영양 변화가 가장 클 때라 환불·교환 정책이 명확해야 해요
5. **재고가 떨어지기 전 알림** — 자동 발송 외에도 다음 배송일을 *언제 변경할 수 있는지* 가 명확한가

파머스테일 정기배송은 매 회차마다 견체중·운동량·BCS를 입력하면 다음 박스의 양과 칼로리가 자동으로 조정돼요. 첫 4주 동안은 보수적으로 시작하고, 우리 아이가 적응한 걸 확인한 후에 정밀 조정으로 들어가요. 일시중지·건너뛰기·해지는 마이페이지에서 1탭이에요.

## 그래서, 우리 아이는?

다음 중 2개 이상이면 정기배송이 의미가 커요.

- 같은 사료를 6개월 이상 안정적으로 먹이고 있음
- 체중·BCS가 일정 범위에서 유지 중
- 이미 알러지 진단이 끝났거나 알러지가 없음
- 사료가 떨어진 날 곤란했던 경험이 있음
- 영양 일관성을 유지하면서 비용도 줄이고 싶음

반대로 다음 중 하나라도 해당되면 단품으로 더 보고 결정해도 좋아요.

- 화식 자체를 처음 시도
- 알러지 진단 진행 중
- 단기 다이어트 / 회복식 등 특수 상황
- 6개월 안에 견 컨디션이 크게 바뀔 예정 (출산·수술 등)

