## BCS는 왜 체중계보다 정확할까

체중은 근육·지방·뼈·수분이 모두 더해진 숫자예요. 우리 아이가 산책을 자주 다녀서 근육이 많을 수도 있고, 노령으로 근육이 빠지면서 지방만 늘었을 수도 있어요. BCS는 **지방이 얼마나 쌓였는지** 를 직접 손과 눈으로 확인하는 방법이라, 견종·골격 차이에 영향을 덜 받아요.

수의영양학 가이드라인에 따르면 BCS 4~5/9(또는 3/5)가 이상적이에요. 이 범위를 유지한 강아지는 그렇지 않은 강아지보다 평균 수명이 **약 1.8년 길었다** 는 Purina의 14년 장기 연구 결과도 있어요.

## 집에서 BCS 확인하는 3가지 포인트

옷을 입은 상태에서는 정확히 볼 수 없어요. 산책 직후 같은 흥분 상태도 피해서, 차분할 때 만져봐요.

### 1. 갈비뼈 — 손바닥 검사

손바닥을 펴서 갈비뼈 위를 가볍게 쓸어봐요.

- **마름(BCS 1~2/5)**: 갈비뼈가 옷 위로도 보이고, 살짝 만져도 뼈가 도드라져요
- **이상(BCS 3/5)**: 갈비뼈가 보이진 않지만 가볍게 만지면 한 줄 한 줄 느껴져요. 손등의 뼈를 만지는 느낌과 비슷해요
- **과체중(BCS 4/5)**: 갈비뼈를 찾으려면 살을 좀 누르고 들어가야 해요
- **비만(BCS 5/5)**: 두꺼운 지방층 때문에 갈비뼈가 거의 안 만져져요

### 2. 허리 라인 — 위에서 본 모습

서 있는 강아지를 위에서 내려다봐요.

- 이상적인 체형은 갈비뼈가 끝나는 지점부터 골반 앞까지 **허리가 살짝 잘록한 모래시계 모양** 이에요
- 직선이거나 오히려 옆구리가 부풀어 보인다면 BCS 4 이상

### 3. 배 라인 — 옆에서 본 모습

옆에서 봤을 때 가슴 쪽 라인이 뒷다리 쪽으로 가면서 **위로 살짝 올라가야** 해요(tuck-up). 가슴부터 배까지 일직선이거나 배가 처져 있다면 지방이 쌓인 신호예요.

## 견종별로 다르게 봐야 해요

한국에서 흔한 견종은 BCS 평가가 좀 까다로워요.

- **말티즈·푸들·비숑**: 털이 풍성해서 눈으로는 거의 알 수 없어요. **반드시 만져서** 확인해주세요
- **포메라니안**: 더블 코트라 더 두꺼워 보여요. 갈비뼈 촉진이 가장 신뢰할 수 있는 지표예요
- **시츄·페키니즈**: 단두종이라 가슴이 넓고 배는 작은 편. 허리 라인 기준을 너무 엄격하게 적용하지 않아도 돼요
- **닥스훈트**: 등이 길어서 배 처짐이 잘 보여요. BCS 4 이상이면 디스크 부담이 커지므로 더 민감하게 관리해주세요

## BCS 4 이상이라면 — 함께 가보아요

체형이 살짝 통통하다고 해서 사료를 갑자기 절반으로 줄이는 건 위험해요. 근육과 지방이 함께 빠지면서 오히려 기초 대사가 떨어져요. 수의영양학에서는 **주당 1~2% 감량** 을 권장해요(예: 5kg 강아지는 한 달에 약 200~400g).

- 현재 사료의 칼로리(kcal/100g) 확인
- 권장량보다 10~15% 줄이고 2주 관찰
- 간식은 하루 총 칼로리의 10% 이내로
- 산책 시간 5분씩 늘리기

작은 변화 4주만 해보면 체형이 보여요. 너무 빠른 변화는 오히려 부담이 돼요.

## 정밀하게 케어하고 싶다면

집에서의 BCS 평가는 ±0.5단계 정도의 오차가 있어요. 더 정확하게 확인하고 싶다면 동물병원의 체중 측정 + DEXA(정밀 체성분 분석) 또는 BIA(생체임피던스) 측정을 한 번 받아두면 좋아요. 비용은 5~10만원 선이지만, 1년에 한 번이면 충분해요.

파머스테일의 화식 박스는 처음 추천 시 BCS를 입력하면 그에 맞춰 일일 권장 칼로리를 보수적으로 잡아드려요. 4주 이후에는 정밀 조정으로 들어가요.

