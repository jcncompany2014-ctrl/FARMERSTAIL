## Q1. 화식만 줘도 영양이 충분한가요?

**가능하지만, 균형이 핵심이에요.**

NRC(미국 국가연구위원회)는 성견에게 필요한 38가지 필수 영양소를 정의해두었어요. 닭가슴살 + 고구마 + 브로콜리만으로는 칼슘이 권장량의 10%도 안 채워져요. 비타민 D, 요오드, 아연도 마찬가지예요.

균형 잡힌 화식이 되려면 다음이 필요해요.

- **단백질**: 체중 1kg당 약 2~5g (성견 기준, FEDIAF)
- **지방**: 총 칼로리의 13% 이상 (EPA·DHA 포함이면 더 좋아요)
- **칼슘 : 인 비율**: 1.2 : 1 ~ 2 : 1 (뼈 분말이나 달걀껍질 분말 보충 필수)
- **필수 비타민·미네랄 프리믹스**: 가정에서 모든 미네랄을 챙기긴 어려워요

수의영양사가 설계한 레시피나, AAFCO 완전영양식(complete and balanced) 기준을 충족한 제품을 쓰는 게 안전해요. 파머스테일 박스는 AAFCO 성견 기준으로 38개 영양소가 모두 들어가도록 설계했어요.

## Q2. 갑자기 바꿔도 괜찮나요?

**절대 안 돼요.** 위장이 적응할 시간이 필요해요.

수의영양학에서는 **10일 전환법** 을 권장해요.

- 1~3일: 기존 사료 75% + 화식 25%
- 4~6일: 기존 사료 50% + 화식 50%
- 7~9일: 기존 사료 25% + 화식 75%
- 10일~: 화식 100%

설사·구토가 있으면 한 단계 뒤로 돌아가고 며칠 더 머물러요. 위장이 예민한 푸들·말티즈·시츄는 14일까지 늘려도 좋아요. 급한 전환은 췌장에 부담을 줘서 췌장염 위험이 올라가요.

## Q3. 한 달에 얼마나 들어요?

견체중과 칼로리 밀도에 따라 다르지만, **5kg 소형견 기준** 으로 대략 이래요.

- 시판 건식 프리미엄 사료: 월 4~7만원
- 직접 만드는 화식(원물 + 프리믹스): 월 12~20만원 (재료비 + 인건비 제외)
- 완전영양식 화식 정기배송: 월 15~25만원

겉으로 보면 화식이 2~3배 비싸 보이지만, 알러지·피부질환·췌장염 같은 만성 이슈가 줄어들면 동물병원 비용이 함께 줄어들 가능성이 있어요. *어떤 게 우리 아이에게 맞는지는 케이스마다 달라요.*

## Q4. 어떤 단백질이 가장 안전한가요?

**닭이 무난하지만, 한 가지에만 의존하지 않는 게 좋아요.**

- **닭가슴살**: 지방 낮고 소화 잘 됨. 한국 강아지 알러지의 약 15%가 닭이라는 보고도 있어요
- **돼지고기**: 비타민 B1이 풍부. 반드시 살코기, 충분히 익혀야 해요
- **연어·고등어**: 오메가-3(EPA·DHA) 풍부. 피부·관절 케어에 좋아요. 단, 수은 위험 때문에 작은 생선이 더 안전해요
- **소고기**: 철분·아연 풍부. 단백질 알러지 발생률은 닭보다 약간 낮은 편

특정 단백질에 6개월 이상 노출되면 알러지 위험이 올라가요. **2~3가지 단백질을 로테이션** 하는 게 면역 다양성에도 좋아요. 파머스테일의 오션 오메가 믹스(연어 베이스)는 닭에 익숙한 아이가 단백질을 다양화하기 좋은 출발점이에요.

## Q5. 익혀서 줘야 하나요, 생식이 좋나요?

수의영양학계는 **익힌 화식(cooked)을 우선 권장** 해요.

- **익혀서 주기**: 살모넬라·캄필로박터·기생충 위험 거의 0. 한국 가정 환경에 가장 안전
- **생식(BARF)**: 영양소 보존은 우수하지만 식중독 위험이 있고, 면역 약한 아이(노령·자견·항암 중)에게는 권장하지 않아요

70~75도로 중심부까지 익히면 영양소 손실은 10~15% 수준이라 큰 차이 없어요. 채소는 살짝 데치거나 찐 후 으깨주면 흡수율이 올라가요.

