- **응급**: 즉시 동물병원

### 3. 포도 · 건포도

극소량으로도 급성 신부전을 일으켜요. 메커니즘은 아직 정확히 밝혀지지 않았어요.

- **위험량**: 개체차 큼. 5kg 강아지 포도 1-2알에 신부전 사례 있음
- **건포도가 더 위험** (농축)
- **증상**: 24시간 이내 구토·식욕 부진·소변량 감소
- **응급**: 1알이라도 먹었으면 즉시 동물병원

### 4. 양파 · 마늘 · 부추 · 쪽파

티오설페이트가 강아지의 적혈구를 파괴해요 (용혈성 빈혈).

- **위험량**: 양파 5kg 강아지 100g, 마늘 5kg 강아지 1톨 (다섯 배 더 독성)
- **증상**: 1-3일 후 갈색 소변·창백한 잇몸·기력 저하
- **숨은 위험**: 갈비탕·김치찌개 국물에 우러나는 향만으로도 누적
- **응급**: 양에 따라 동물병원

### 5. 마카다미아너트

원인 미상이지만 강아지에게 특히 위험.

- **위험량**: 체중 1kg당 2g (5kg 강아지 10g)
- **증상**: 12시간 내 뒷다리 마비·구토·고열
- **응급**: 즉시 동물병원

### 6. 알코올

작은 양으로도 강아지는 의식 잃어요.

- **위험량**: 사람의 1/10 정도. 맥주 한 모금도 위험
- **숨은 위험**: 빵 반죽 (효모가 발효되며 알코올 생성)
- **증상**: 30분 내 휘청거림·구토·체온 저하
- **응급**: 즉시 동물병원

### 7. 카페인

커피·녹차·에너지 드링크·다이어트 알약.

- **위험량**: 체중 1kg당 50mg. 5kg 강아지 에스프레소 1잔에 위험
- **증상**: 1-2시간 내 과흥분·빈맥·발작
- **응급**: 즉시 동물병원

## 만성 손상을 일으키는 — 5가지

### 8. 호두 · 피칸 · 헤이즐넛

곰팡이 독소 (mycotoxin) 위험. 즉시 위험은 아니지만 누적 시 간 손상.

### 9. 아보카도

페르신 (persin) 이 강아지 심장에 무리. 과육은 적은 양이면 OK 라는 의견도 있지만 **씨앗·껍질은 절대 금지**.

### 10. 생 효모 반죽 (빵 반죽)

위 속에서 발효되며 알코올 생성 + 풍선처럼 부풀어 위가 꼬일 수 있어요 (위염전).

### 11. 짠 음식 (라면·삼각김밥·소시지)

체중 1kg당 나트륨 1g 이상이면 위험. 만성 섭취는 신장·심장 부담.

### 12. 익히지 않은 뼈

작은 뼈는 갈라져 식도·장을 찢을 수 있어요. **익힌 닭 뼈가 가장 위험** (잘 부서져 날카로워짐).

